const I18N = (() => {
  const saved = Store.get('ol_lang');
  const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
  let lang = saved || (STRINGS[nav] ? nav : 'en');
  return {
    t: (key) => (STRINGS[lang] && STRINGS[lang][key]) || STRINGS.en[key] || key,
    get lang() { return lang; },
    set lang(l) { if (STRINGS[l]) { lang = l; Store.set('ol_lang', l); } },
    cycle() {
      const keys = Object.keys(STRINGS);
      this.lang = keys[(keys.indexOf(lang) + 1) % keys.length];
      return lang;
    },
  };
})();
